public class DeclarandoVariaveis {

    public static void main(String[] args) {

//      <tipo> <nome-da-variavel> = <valor-atribuido>
        int idade = 33;
        double altura = 1.58;
        char sexo = 'F';

        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Sexo: " + sexo);

    }
}
